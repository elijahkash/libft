#include "ft_diff_utils.h"

#include "ft_memory_manager.h"

char	*ft_strnew(size_t size)
{
	char *str;

	if (size == __SIZE_MAX__)
		return (NULL);
	str = (char *)ft_memalloc(size + 1);
	return (str);
}
