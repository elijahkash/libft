#include "ft_libc.h"

#ifdef USE_LIBC

inline void	ft_bzero(void *s, size_t n)
{
	bzero(s, n);
}

#else

void		ft_bzero(void *s, size_t n)
{
	ft_memset(s, 0, n);
	return ;
}

#endif
