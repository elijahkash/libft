#include "ft_libc.h"

#ifdef USE_LIBC

# include <ctype.h>

inline int	ft_isalpha(int c)
{
	return (isalpha(c));
}

#else

int			ft_isalpha(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	else
		return (0);
}

#endif
